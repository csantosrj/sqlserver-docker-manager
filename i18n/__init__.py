
"""
Simple runtime internationalization for SQL Server Docker Manager.

This project intentionally uses a small dictionary-based translator instead
of Qt .qm files to keep the application easy to package with PyInstaller.
"""

from __future__ import annotations

CURRENT_LANGUAGE = "pt_PT"

LANGUAGES: dict[str, str] = {
    "pt_PT": "Português (Portugal)",
    "pt_BR": "Português (Brasil)",
    "en": "English",
    "es": "Español",
    "fr": "Français",
}


def available_languages() -> list[tuple[str, str]]:
    return list(LANGUAGES.items())


def language_label(code: str) -> str:
    return LANGUAGES.get(code, LANGUAGES["pt_PT"])


def set_language(code: str) -> None:
    global CURRENT_LANGUAGE
    CURRENT_LANGUAGE = code if code in LANGUAGES else "pt_PT"


def get_language() -> str:
    return CURRENT_LANGUAGE


def tr(text: str) -> str:
    if text is None:
        return ""
    if CURRENT_LANGUAGE == "pt_PT":
        return text
    return TRANSLATIONS.get(CURRENT_LANGUAGE, {}).get(text, text)


